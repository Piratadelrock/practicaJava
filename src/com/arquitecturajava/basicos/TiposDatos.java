package com.arquitecturajava.basicos;

public class TiposDatos {
    public static void main(String[] args) {
        byte numByte1 = 5;
        short numShort2 = 20000;
        int numInt3 = 1000000;
        long numLong4 = 2000000000;
        System.out.println(numByte1);
        System.out.println(numShort2);
        System.out.println(numInt3);
        System.out.println(numLong4);

        float numFloat5 = 20.20F;
        double numDouble6 = 20000.30;
        System.out.println(numFloat5);
        System.out.println(numDouble6);

        char c= 'a';
        System.out.println(c);

        boolean verdadero = true;
        System.out.println(verdadero);
    }
}
