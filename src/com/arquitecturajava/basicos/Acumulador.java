package com.arquitecturajava.basicos;

public class Acumulador {
    public static void main(String[] args) {

        int aprobados = 3;
        System.out.println(aprobados);
        aprobados++;
        System.out.println(aprobados);

        int nota1=5;
        int nota2 = nota1 + 2;
        System.out.println(nota2);
        nota2++;
        System.out.println(nota2);
        nota2--;
        System.out.println(nota2);



    }
}
